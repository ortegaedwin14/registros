    public void mostrarTabla(String nombreaso) {
        String Consulta;
        if (txt_nombreAso.getText().isBlank()) {
            Consulta = "SELECT * FROM asosiaciones";
        } else {
            Consulta = "SELECT * FROM asosiaciones WHERE nombre_aso like'%"+nombreaso+"%'";
        }
        Statement st;
        ConexionRegistro con = new ConexionRegistro();
        Connection ConexionRegistro = (Connection) con.conectar();

        // metodo para evitar que editen la tabla
        DefaultTableModel modelo = new DefaultTableModel();
        // añadir columnas 

        modelo.addColumn("NOMBRE DE ASOCIACIONES");
 

        Asociacion.setModel(modelo);

        // llenar datos obtenidos de la bdd (Consulta)
        String[] datos = new String[1];
        try {
            st = (Statement) ConexionRegistro.createStatement();
            ResultSet resul = st.executeQuery(Consulta);
            while (resul.next()) {

                datos[0] = resul.getString(2);

                modelo.addRow(datos);
            }
            resul.close();
            Asociacion.setModel(modelo);

        } catch (Exception e) {
            JOptionPane.showInternalMessageDialog(null, "error" + e.toString());
        }

    }



    private void AsociacionMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_AsociacionMouseClicked
        // TODO add your handling code here:
        String nombre = txt_nombreAso.getText();
        int indice = Asociacion.getSelectedRow();
        String consulta = "SELECT * FROM registros.asosiaciones LIMIT " + indice + "," + 1;
        //INICIAR SESIÓN A NIV
        try {
            sesion = (PreparedStatement) conectado.prepareStatement(consulta);
            ResultSet resul = sesion.executeQuery();
            while (resul.next()) {
                id_aso = resul.getInt(1);
            }
        } catch (SQLException e) {
            Component rootPane = null;
            JOptionPane.showMessageDialog(rootPane, "NO SE PUEDE ELIMINAR");
        }

    }//GEN-LAST:event_AsociacionMouseClicked


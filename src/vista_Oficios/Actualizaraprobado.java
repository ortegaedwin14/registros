    private void btn_actualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_actualizarActionPerformed
       if (txt_abrobado.getText().isEmpty()|| txt_pdfs.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Por favor, complete todos los campos.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
         try {
            String fecha = formatfecha(fecha_e.getValue().toString());
             
                            Registro_oficio r1 = new Registro_oficio();
                            r1.setId_of(Oficio_F.id_of);
                            r1.setFecha_entrega(fecha);
                            r1.setAprobado(txt_abrobado.getText());
                            r1.setPdf(txt_pdfs.getText());
                            Registro_OficioControlador rc  = new Registro_OficioControlador();
                            rc.ActualizarAprobado(fecha, txt_abrobado.getText(), txt_pdfs.getText(),Oficio_F.id_of);
                                          
     } catch (Exception e) {
            JOptionPane.showInternalMessageDialog(null, "error" + e.toString());
        }
    
    }//GEN-LAST:event_btn_actualizarActionPerformed


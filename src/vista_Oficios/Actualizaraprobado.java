    public String formatfecha (String spinner){
        int mes = 0;
        String[] partes = spinner.split(" ");
        switch (partes[1]){
            case "Jan" -> mes = 1;
            case "Feb" -> mes = 2;
            case "Mar" -> mes = 3;
            case "Apr" -> mes = 4;
            case "May" -> mes = 5;
            case "Jun" -> mes = 6;
            case "Jul" -> mes = 7;
            case "Aug" -> mes = 8;
            case "Sep" -> mes = 9;
            case "Oct" -> mes = 10;
            case "Nov" -> mes = 11;
            case "Dec" -> mes = 12;  
        }
        return partes[5]+ "-" + mes + "-" + partes[2];
    }
    

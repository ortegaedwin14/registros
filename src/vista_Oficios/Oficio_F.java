    public void mostrarTabla(String cedula  ) {
        String Consulta;
        if (txt_cedulaO.getText().isBlank()) {
            Consulta = "SELECT * FROM registro_oficio";
        } else {
            Consulta = "SELECT * FROM registro_oficio where fkres_cedula LIKE '" + cedula + "%'";
        }

        Statement st;
        ConexionRegistro con = new ConexionRegistro();
        Connection ConexionRegistro = (Connection) con.conectar();

        // metodo para evitar que editen la tabla
        DefaultTableModel modelo = new DefaultTableModel();
        // a√±adir columnas 
        modelo.addColumn("NUMERO_OF");
        modelo.addColumn("SOLICITUD");
        modelo.addColumn("OBSERVACIONES");
        modelo.addColumn("RESPONSABLE");
        modelo.addColumn("FECHA_RESEPCION");
        modelo.addColumn("FECHA_ENTREGA");
        modelo.addColumn("CEDULA");
        modelo.addColumn("ASOCIACION");
        modelo.addColumn("APROBADO");
        modelo.addColumn("ARCHIVO PDF");

        Oficio.setModel(modelo);

        // llenar datos obtenidos de la bdd (Consulta)
        String[] datos = new String[11];
        try {
            st = (Statement) ConexionRegistro.createStatement();
            ResultSet resul = st.executeQuery(Consulta);
            while (resul.next()) {
                datos[0] = resul.getString(2);
                datos[1] = resul.getString(3);
                datos[2] = resul.getString(4);
                datos[3] = resul.getString(5);
                datos[4] = resul.getString(6);
                datos[5] = resul.getString(7);
                datos[6] = resul.getString(8);
                datos[7] = resul.getString(9);
                datos[8] = resul.getString(10);
                datos[9] = resul.getString(11);

                modelo.addRow(datos);
            }
            resul.close();
            Oficio.setModel(modelo);

        } catch (Exception e) {
            JOptionPane.showInternalMessageDialog(null, "error" + e.toString());
        }

    }



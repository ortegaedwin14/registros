    private void OficioMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_OficioMouseClicked
        // TODO add your handling code here:
        String cedula = txt_cedulaO.getText();
        int indice = Oficio.getSelectedRow();
        String consulta = "SELECT * FROM registros.registro_oficio  LIMIT " + indice + "," + 1;
        //INICIAR SESI√ìN A NIV
        try {
            sesion = (PreparedStatement) conectado.prepareStatement(consulta);
            ResultSet resul = sesion.executeQuery();
            while (resul.next()) {
                id_of = resul.getInt(1);
            }
        } catch (SQLException e) {
            Component rootPane = null;
            JOptionPane.showMessageDialog(rootPane, "NO EXISTE");
        }
       
    }//GEN-LAST:event_OficioMouseClicked

    private void btn_eliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_eliminarActionPerformed
        if (id_of == 0){
            JOptionPane.showMessageDialog(rootPane, "POR FAVOR SELECCIONE UN OFICIO");
            return;
        }
        Registro_OficioControlador ec = new Registro_OficioControlador();
        int id1;
        String cedulaO;
      
                            
        try {
            ec.buscarOficio(Integer.parseInt(txt_cedulaO.getText()));
            id1 = ec.comprobarOficio( Oficio.getEditingRow());
            ec.eliminarOficio(Oficio_F.id_of);
        } catch (SQLException ex) {
            Logger.getLogger(Oficio_F.class.getName()).log(Level.SEVERE, null, ex);
        }
                            
    }//GEN-LAST:event_btn_eliminarActionPerformed

